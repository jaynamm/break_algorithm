#include <iostream>

using namespace std;

int main(){

    int t, n;
    int dp[11];
    cin >> t;

    dp[1] = 1; // > 1
    dp[2] = 2; // 1+1, 2 > 2
    dp[3] = 4; // 1+1+1 / 1+2, 2+1 / 3 > 4
    // 4 : 1+1+1+1 / 1+1+2, 1+2+1, 2+1+1, 2+2 / 1+3, 3+1 > 7
    // 5 : 1+1+1+1+1 / 1+1+1+2, 1+1+2+1, 1+2+1+1, 2+1+1+1, 1+2+2, 2+1+2, 2+2+1
    //     / 1+1+3, 1+3+1, 3+1+1 / 3+2, 2+3> 13
    // 6 : 111111 / 11112 11121 11211 12111 21111 / 1122 1212 1221 2112 2121 2211 / 222
    //        1113 1131 1311 3111 / 123 132 213 231 312 321 / 33 > 24
    //
    // 7 : 44

    for(int i=4; i<=10; i++){
        dp[i] = dp[i-1] + dp[i-2] + dp[i-3];
    }


    for(int i=0; i<t; i++){
        cin >> n;
        cout << dp[n] << "\n";
    }

    return 0;
}
